# List -> array, mengaksesnya dengan menggunakan index

data_list = ['alex', 'asep', 'ujang']

print(data_list[1])

# Dictionary (dict) -> associative array
# Mengaksesnya dengan indentifier -> key

data_dict = {
    'key':'value',
    'lx':'alex',
    'sp':'asep',
    'jg':'ujang',
    'nmr':190,
    'list':data_list
}

print(data_dict['lx'])
print(data_dict['nmr'])
print(data_dict['list'])