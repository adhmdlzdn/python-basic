# ELIF = else if statement

nama = input("Nama kamyu siapa? ")

if nama == "ucup": # kondisi 1
    print("Hai ganteeennggg beudds!") # aksi kondisi 1
elif nama == "otong": # kondisi 2
    print("Hai si kece bangeeeeetsss!") # aksi kondisi 2
elif nama == "mario": # kondisi 3
    print("Hai humooooriieesshhh!") # aksi kondisi 3
else:
    print("Au ah gak kenal!")

print("Ini adalah akhir dari program")